# MALO Risk Prediction — Shiny App
# Random Survival Forest (competing risks) for Major Adverse Liver Outcomes
#
# HOW A SHINY APP WORKS — three-paragraph orientation for new users
# ─────────────────────────────────────────────────────────────────
# A Shiny app is split into two pieces that talk to each other:
#
# 1.  ui  (User Interface) — defines the *layout and widgets* the browser
#     shows.  Think of it as the HTML template: input controls on the left,
#     output placeholders on the right.  It runs once, when the page loads.
#
# 2.  server — contains the *R logic* that runs in response to user actions.
#     Every time the user clicks a button or changes a slider, Shiny re-runs
#     the relevant reactive blocks inside server and pushes new values into
#     the output placeholders defined in ui.
#
# The call shinyApp(ui, server) at the bottom ties them together.
#
# REQUIRED FILES (must be in the same folder as app.R)
# ─────────────────────────────────────────────────────
#   RSF_final_clinical_model.rds  — saved with saveRDS() in BFA01.1.2
#   background_data.rds           — generated by generate_background.R
###############################################################################


# ── 1.  Packages ──────────────────────────────────────────────────────────────
# shiny:           the web-app framework
# bslib:           Bootstrap 5 theme helpers (cards, layouts, colour themes)
# randomForestSRC: the package the RSF model was trained with — needed to call
#                  predict() on the saved model object
# kernelshap:      model-agnostic Kernel SHAP values (replaces shapr/fastshap)
# dplyr:           data-wrangling (filter, mutate, etc.)
library(shiny)
library(bslib)
library(randomForestSRC)
library(kernelshap)
library(dplyr)

# ── 2.  Load heavy objects at start-up (OUTSIDE the server function) ──────────
# Placing readRDS() here means R loads the model into memory *once* when the
# app process starts, and then shares that single copy across all user
# sessions.  If you put it inside server(), every new visitor would re-read
# the 1 GB file — slow and wasteful.
#
# The model file is too large to ship with the app, so it is downloaded from
# Dropbox on first launch and cached locally for the lifetime of this process.
local({
  model_path <- file.path(tempdir(), "RSF_final_clinical_model.rds")
  if (!file.exists(model_path)) {
    message("Downloading RSF model from Dropbox (this may take a minute) …")
    download.file(
      url      = "https://www.dropbox.com/scl/fi/ulveqiv49gcx6jlydhtz7/RSF_final_clinical_model.rds?rlkey=50bkggvk08gagekozy4dlh9m1&st=w4ypd1cp&dl=1",
      destfile = model_path,
      mode     = "wb"
    )
    message("Download complete.")
  }
  message("Loading RSF model …")
  model <<- readRDS(model_path)
})

message("Loading SHAP background data …")
background <- readRDS("background_data.rds")
# background is a data.frame with 200–300 rows × 6 predictor columns,
# all stored as plain numbers (no factors).  It represents a sample of
# the training population and is used by shapr as the reference
# distribution when estimating each feature's marginal contribution.


# ── 3.  UI ────────────────────────────────────────────────────────────────────
# page_sidebar() gives a fixed left sidebar and a scrollable main area.
# All widget functions follow the pattern:
#   widgetType(inputId, label, ...)
# The inputId is the name you use in server to read the widget's value
# as input$<inputId>.

ui <- page_sidebar(
  title = "MALO Risk Prediction",
  theme = bs_theme(bootswatch = "flatly"),

  # ── Left sidebar: input controls ──────────────────────────────────────────
  sidebar = sidebar(
    width = 310,

    h5("Patient Characteristics", class = "mt-1 mb-3"),

    numericInput(
      inputId = "age",
      label   = "Age (years)",
      value   = 55, min = 18, max = 100, step = 1
    ),

    selectInput(
