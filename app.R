      choices = c("Male", "Female")
    ),

    numericInput(
      inputId = "bmi",
      label   = "BMI (kg/m²)",
      value   = 27.0, min = 10, max = 70, step = 0.1
    ),

    selectInput(
      inputId = "t2dm",
      label   = "Type 2 Diabetes",
      # The *value* sent to server is "0" or "1"; the *label* is what the
      # user sees in the drop-down.
      choices = c("No" = "0", "Yes" = "1")
    ),

    numericInput(
      inputId = "alcohol",
      label   = "Alcohol (standard drinks / week)",
      value   = 5, min = 0, max = 250, step = 1
    ),
    helpText("1 standard drink = 14 g alcohol.  Values above ~36 drinks/week
              (500 g) are capped at 500 g in line with the training data."),

    selectInput(
      inputId = "smoking",
      label   = "Smoking status",
      choices = c(
        "Non-current smoker (never / ex)" = "0",
